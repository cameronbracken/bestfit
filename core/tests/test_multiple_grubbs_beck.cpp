// --- Test_GrubbsBeck ------------------------------------------------------

void test_grubbs_beck() {
    // Table 1.2 Maximum annual peak discharge values in cms, observed at the
    // Harricana River at Amos (Quebec, Canada)
    std::vector<double> data = {
        122, 244, 214, 173, 229, 156, 212, 263, 146, 183, 161, 205, 135, 331,
        225, 174, 98.8, 149, 238, 262, 132, 235, 216, 240, 230, 192, 195, 172,
        173, 172, 153, 142, 317, 161, 201, 204, 194, 164, 183, 161, 167, 179,
        185, 117, 192, 337, 125, 166, 99.1, 202, 230, 158, 262, 154, 164, 182,
        164, 183, 171, 250, 184, 205, 237, 177, 239, 187, 180, 173, 174};

    double x_hi = 0.0, x_lo = 0.0;
    MultipleGrubbsBeckTest::grubbs_beck_test(data, x_hi, x_lo);

    CHECK_NEAR(x_hi, 378.2, 1e-1);  // page 9 of reference.
    CHECK_NEAR(x_lo, 91.2, 1e-1);
}

void test_grubbs_beck_throws_on_nonpositive() {
    std::vector<double> data = {1.0, 2.0, 0.0, 4.0};
    double x_hi = 0.0, x_lo = 0.0;
    CHECK_THROWS(MultipleGrubbsBeckTest::grubbs_beck_test(data, x_hi, x_lo));
}

