// Oracle values are transcribed directly from the C# unit tests in
// upstream/Numerics/Test_Numerics/Data/Statistics/Test_HypothesisTests.cs:
//   - Test_GrubbsBeck()          -- Harricana River (Amos, Quebec) annual peaks;
//                                   expected XHi/XLo from "The Gamma Family..." p. 9.
//   - Test_MultipleGrubbsBeck()  -- USGS station datasets (BLU, CGR, FCK, FOS, GPV,
//                                   HCK, LOP) plus a synthetic sample, each with an
//                                   expected low-outlier count validated upstream
//                                   against HEC-SSP / R.
// Tolerances mirror the C# Assert.AreEqual tolerances (1e-1 for GrubbsBeck; exact
// integer equality for the multiple Grubbs-Beck counts).
